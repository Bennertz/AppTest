package sp.senai;

public class Calculadora {
    // Método somar
    public int somar(int n1, int n2) {
        int resultado;
        resultado = n1 + n2;
        return resultado;
    }
    // Método subtrair
    public int subtrair(int n1, int n2) {
        int resultado;
        resultado = n1 - n2;
        return resultado;
    }
    // Método multiplicar
    public int multiplicar (int n1, int n2) {
        int resultado;
        resultado = n1 * n2;
        return resultado;
    }
    // Método dividir
    public int dividir (int n1, int n2) {
        int resultado;
        resultado = n1 / n2;
        return resultado;
    }
}
